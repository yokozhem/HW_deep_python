"""
2. Напишите функцию принимающую на вход только ключевые параметры и возвращающую словарь, 
где ключ — значение переданного аргумента, а значение — имя аргумента. 
Если ключ не хешируем, используйте его строковое представление.
"""

def keyword_args_to_dict(**kwargs):
    """
    Функция принимает только ключевые параметры и возвращает словарь,
    где ключ - значение переданного аргумента, а значение - имя аргумента.

    Аргументы:
    **kwargs: Ключевые параметры.

    Возвращает:
    dict: Словарь, где ключ - значение переданного аргумента,
    а значение - имя аргумента.
    """
    result = {}
    for key, value in kwargs.items():
        # Преобразуем значение в строку, если оно не хешируемо
        key_str = str(value) if hasattr(value, '__hash__') else value
        result[key_str] = key
    return result

# Пример использования функции
args_dict = keyword_args_to_dict(a=1, b="hello", c=[1, 2, 3], d=None)
print(args_dict)
